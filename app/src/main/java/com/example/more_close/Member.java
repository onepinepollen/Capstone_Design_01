package com.example.more_close;

public class Member {
    private String id;
    private String name;
    private String member;

    public void setId(String a) {
        this.id = a;
    }
    public String getId(){
        return id;
    }
    public void setName(String a) {
        this.name = a;
    }
    public String getName(){
        return name;
    }
    public void setMember(String a) {
        this.member = a;
    }
    public String getMember(){
        return member;
    }
}
