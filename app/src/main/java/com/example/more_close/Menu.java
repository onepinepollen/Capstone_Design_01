package com.example.more_close;

public class Menu {
    private String id;
    private String name;
    private String menu;
    private String pay;

    public void setId(String a) {
        this.id = a;
    }
    public String getId(){
        return id;
    }
    public void setName(String a) {
        this.name = a;
    }
    public String getName(){
        return name;
    }
    public void setMenu(String a) {
        this.menu = a;
    }
    public String getMenu() {
        return menu;
    }
    public void setPay(String a) {
        this.pay = a;
    }
    public String getPay(){
        return pay;
    }
}
